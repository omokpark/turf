"""상호명 정규화 — 지점명·괄호·법인표기 제거의 단일 출처

signals/franchise.py의 임시 근사치를 이관·확장한 것. 목적은 "같은 브랜드/업소인가"를
문자열로 판별할 수 있게 잡음을 걷어내는 것이며, 과도한 제거로 서로 다른 업소가
합쳐지는 것(오병합)보다 덜 제거되는 것(미병합)을 택한다.
"""

import re

# 법인·상법 표기 (위치 무관)
_CORP_RE = re.compile(r"(주식회사|\(주\)|\(유\)|유한회사|농업회사법인|\(재\)|\(사\))")
# 괄호 안 부가 표기: (강남점), (2호점), (Noa) 등
_PAREN_RE = re.compile(r"[\(（].*?[\)）]")
# 지점명 접미사 — 끝에 붙은 경우만, 명시적 목록으로 보수적으로 (이름 중간의 '점'은 보존).
# '강남역점'처럼 지역명이 남는 경우('...강남')는 감수한다 — 지역 토큰까지 지우려면
# 지명 사전이 필요해 오병합 위험이 커진다.
_BRANCH_SUFFIX_RE = re.compile(r"(역점|본점|분점|직영점|지점|[0-9]+호점|점)$")


def normalize_name(name: str) -> str:
    """상호명에서 법인표기·괄호·말미 지점명을 제거하고 공백을 없앤다.

    제거 후 2글자 미만이 되면(예: '강남점' 단독 상호) 과제거로 보고 원형(공백만 정리)을
    유지한다 — 오병합 방지 우선 원칙.
    """
    raw = re.sub(r"\s+", "", str(name))
    n = _CORP_RE.sub("", str(name))
    n = _PAREN_RE.sub("", n).strip()
    n = re.sub(r"\s+", "", n)
    n = _BRANCH_SUFFIX_RE.sub("", n)
    if len(n) < 2:
        return raw
    return n
